#!/usr/bin/env node
/**
 * ai-garden · Autopilot PR Summary
 *
 * Builds a deterministic pull request title/body after daily-evolution.js has
 * mutated the world. GitHub Actions consumes the outputs to open a draft PR.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const WORLD = path.join(ROOT, 'experiments', 'world-state.json');
const README = path.join(ROOT, 'README.md');

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

function safeLine(text, fallback, maxLength) {
  const max = maxLength || 140;
  return String(text || fallback || 'the garden evolved')
    .replace(/\s+/g, ' ')
    .replace(/[^\w .,:;!?'"()/#&+-]/g, '')
    .trim()
    .slice(0, max);
}

function eventCounts(events) {
  return events.reduce((counts, event) => {
    const key = event && event.kind ? event.kind : 'unknown';
    counts[key] = (counts[key] || 0) + 1;
    return counts;
  }, {});
}

function markdownList(items) {
  return items.length ? items.map(item => '- ' + item).join('\n') : '- No notable entries recorded.';
}

function latestHistory(world) {
  const history = Array.isArray(world.history) ? world.history : [];
  return history[history.length - 1] || {};
}

function liveBlock(readme) {
  const match = readme.match(/<!-- live:start -->([\s\S]*?)<!-- live:end -->/);
  return match ? match[1].trim() : 'Live block not found.';
}

function appendGithubOutput(outputs) {
  if (!process.env.GITHUB_OUTPUT) return;
  const chunks = [];
  for (const [key, value] of Object.entries(outputs)) {
    const text = String(value);
    if (text.includes('\n')) {
      chunks.push(`${key}<<EOF_${key}\n${text}\nEOF_${key}`);
    } else {
      chunks.push(`${key}=${text}`);
    }
  }
  fs.appendFileSync(process.env.GITHUB_OUTPUT, chunks.join('\n') + '\n');
}

function main() {
  const outPath = process.argv[2] || path.join(process.env.RUNNER_TEMP || ROOT, 'autopilot-pr-body.md');
  const world = readJson(WORLD);
  const readme = fs.readFileSync(README, 'utf8');
  const latest = latestHistory(world);
  const day = world.chronicle && world.chronicle.day ? world.chronicle.day : 0;
  const headline = safeLine((latest.headline || '').replace(/^[^A-Za-z0-9]+/, ''), 'the garden evolved', 120);
  const title = `[autopilot] Daily Evolution PR · Day ${day} — ${headline.slice(0, 72)}`;
  const events = Array.isArray(latest.events) ? latest.events : [];
  const counts = eventCounts(events);
  const discoveries = events.filter(event => event.kind === 'discovery').map(event => safeLine(event.headline, '', 180));
  const society = events
    .filter(event => ['city', 'religion', 'dynasty', 'tech', 'myth', 'omen', 'market', 'festival'].includes(event.kind))
    .slice(0, 8)
    .map(event => `${event.kind}: ${safeLine(event.headline, '', 180)}`);
  const risks = [
    'Draft PR only: no auto-merge is enabled.',
    'Generated branch exits early if another autopilot PR is already open.',
    'World JSON, README live block, and playtest checks are validated before PR creation.'
  ];
  const stats = [
    `Day: ${day}`,
    `Alive citizens: ${(world.citizens || []).filter(c => c.alive).length}`,
    `Remembered citizens: ${(world.citizens || []).filter(c => !c.alive).length}`,
    `Regions: ${world.map && world.map.regions ? world.map.regions.length : 0}`,
    `Map: ${world.map ? world.map.width + 'x' + world.map.height : 'unknown'}`,
    `Cities: ${(world.cities || []).length}`,
    `Active wars: ${(world.wars || []).filter(w => w.active).length}`,
    `Religions: ${(world.religions || []).length}`,
    `Techs: ${(world.technologies || []).length}/20`
  ];
  const countLine = Object.keys(counts).sort().map(key => `${key} ${counts[key]}`).join(', ') || 'none';
  const body = [
    '## Autopilot Summary',
    '',
    `This draft PR was generated by the AI Garden Autopilot after running \`scripts/daily-evolution.js\` for Day ${day}. It is meant to give the garden an automatic reviewable loop instead of silently landing every generated change.`,
    '',
    '## Live State',
    '',
    liveBlock(readme),
    '',
    '## World Changes',
    '',
    `Event mix: ${countLine}.`,
    '',
    markdownList(society),
    '',
    '## Map Discoveries',
    '',
    markdownList(discoveries),
    '',
    '## Stats',
    '',
    markdownList(stats),
    '',
    '## Safety',
    '',
    markdownList(risks),
    '',
    '## Validation',
    '',
    '- `node --check scripts/daily-evolution.js`',
    '- `node --check scripts/civilization-brain.js`',
    '- `node --check scripts/civilization-visuals.js`',
    '- `node --check scripts/society-director.js`',
    '- `node --check scripts/playtest-subagent.js`',
    '- `jq empty experiments/world-state.json`',
    '- `node scripts/playtest-subagent.js`'
  ].join('\n');

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, body + '\n');
  appendGithubOutput({ title, body_path: outPath, day, headline });
  console.log('Autopilot PR summary written: ' + outPath);
  console.log(title);
}

main();
